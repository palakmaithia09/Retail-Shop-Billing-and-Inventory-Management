    private void sendOrderMail(String toEmail, String orderId, String orderDate, String netAmount) {

    final String fromEmail = "palakmaithia@gmail.com";
    final String password = "fnhdojdwxzkbpvnf";

    // ðŸ”¹ Generate PDF
    String pdfPath = generateBillPDF(orderId, orderDate, netAmount);

    Properties props = new Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587");

    Session session = Session.getInstance(props, new Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(fromEmail, password);
        }
    });
    emailExecutor.submit(()->{

    try {
        Message msg = new MimeMessage(session);
        msg.setFrom(new InternetAddress(fromEmail));
        msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(toEmail));
        msg.setSubject("Order Bill - Order ID " + orderId);

        // ðŸ”¹ Mail body
        MimeBodyPart textPart = new MimeBodyPart();
        String html =
            "<h2>Order Bill</h2>" +
            "<p>Please find attached your bill.</p>" +
            "<b>Order ID:</b> " + orderId + "<br>" +
            "<b>Order Date:</b> " + orderDate + "<br>" +
            "<b>Net Amount:</b> â‚¹" + netAmount;

        textPart.setContent(html, "text/html");

        // ðŸ”¹ PDF attachment
        MimeBodyPart pdfPart = new MimeBodyPart();
        pdfPart.attachFile(new File(pdfPath));

        Multipart multipart = new MimeMultipart();
        multipart.addBodyPart(textPart);
        multipart.addBodyPart(pdfPart);

        msg.setContent(multipart);

        Transport.send(msg);
        Thread.sleep(3000);
        JOptionPane.showMessageDialog(this, "Mail sent with PDF bill!");

    } catch (HeadlessException | IOException | InterruptedException | MessagingException e) {
        JOptionPane.showMessageDialog(this, "Mail Error: " + e.getMessage());
    }finally
    {
        emailExecutor.shutdown();
    }
    });
}


