    // ================= HOVER EFFECT =================
    private int hoverRow = -1;
    private int hoverCol = -1;

    private void addHoverEffect() {

        jTable1.addMouseMotionListener(new MouseAdapter() {
            @Override
            public void mouseMoved(MouseEvent e) {
                hoverRow = jTable1.rowAtPoint(e.getPoint());
                hoverCol = jTable1.columnAtPoint(e.getPoint());
                jTable1.repaint();
            }
        });

        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseExited(MouseEvent e) {
                hoverRow = -1;
                hoverCol = -1;
                jTable1.repaint();
            }
        });

        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(
                    JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {

                Component c = super.getTableCellRendererComponent(
                        table, value, isSelected, hasFocus, row, column);

                // Default styling
                c.setBackground(Color.WHITE);
                c.setForeground(new Color(0, 0, 102));
                c.setFont(new Font("Rockwell", Font.PLAIN, 11));

                // Hover Row Effect
                if (row == hoverRow) {
                    c.setBackground(new Color(230, 230, 250)); // light lavender
                    c.setFont(new Font("Rockwell", Font.BOLD, 12));
                }

                // Hover Column Effect
                if (column == hoverCol) {
                    c.setBackground(new Color(204, 229, 255)); // light blue
                    c.setFont(new Font("Rockwell", Font.BOLD, 12));
                }

                // Selected Row Effect (override hover)
                if (isSelected) {
                    c.setBackground(new Color(160, 200, 255));
                    c.setForeground(Color.BLACK);
                    c.setFont(new Font("Rockwell", Font.BOLD, 12));
                }

                return c;
            }
        });
    }

