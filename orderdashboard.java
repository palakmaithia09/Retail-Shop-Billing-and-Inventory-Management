    private void addTableUpdateListener() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.addTableModelListener(e -> {
            if (e.getType() != TableModelEvent.UPDATE) return;

            int row = e.getFirstRow();
            int col = e.getColumn();

            Object newValue = model.getValueAt(row, col);
            int orderId = Integer.parseInt(model.getValueAt(row, 0).toString());

            String columnName = model.getColumnName(col);
            updateOrderDatabase(columnName, newValue, orderId);
        });
    }

    private void updateOrderDatabase(String column, Object value, int orderId) {

        String sql;

        switch (column) {
            case "MRP" -> sql = "UPDATE order_book SET mrp=? WHERE o_id=?";
            case "Discount" -> sql = "UPDATE order_book SET discount=? WHERE o_id=?";
            case "Net Amount" -> sql = "UPDATE t_ransaction SET net_amt=? WHERE orderid=?";
            case "Payment Mode" -> sql = "UPDATE t_ransaction SET payment_mode=? WHERE orderid=?";
            case "Transaction Date" -> sql = "UPDATE t_ransaction SET tdate=? WHERE orderid=?";
            default -> {
                return;
            }
        }

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/retailbilling",
                "root", "PFH#9kgrw");
             PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setObject(1, value);
            pst.setInt(2, orderId);
            pst.executeUpdate();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
